def get_seed_documents() -> List[Document]:
    """获取所有种子文档"""
    documents = []

    # 添加安全规范
    for reg in SAFETY_REGULATIONS:
        chunks = load_and_chunk(
            text=reg["content"],
            title=reg["title"],
            source=reg["source"],
            category=reg["category"]
        )
        documents.extend(chunks)

    # 添加事故案例
    for case in ACCIDENT_CASES:
        chunks = load_and_chunk(
            text=case["content"],
            title=case["title"],
            source=case["source"],
            category=case["category"]
        )
        documents.extend(chunks)

    # 添加检查表模板
    for checklist in CHECKLIST_TEMPLATES:
        chunks = load_and_chunk(
            text=checklist["content"],
            title=checklist["title"],
            source=checklist["source"],
            category=checklist["category"]
        )
        documents.extend(chunks)

    return documents


async def seed_all_knowledge():
    """初始化种子数据到知识库"""
    try:
        documents = get_seed_documents()
        vector_store = create_vector_store(embed_dim=768)

        success = vector_store.upsert(documents)
        if success:
            logger.info(f"Successfully seeded {len(documents)} knowledge chunks")
        else:
            logger.error("Failed to seed knowledge")

        return success

    except Exception as e:
        logger.error(f"Seed knowledge error: {e}")
        return False


